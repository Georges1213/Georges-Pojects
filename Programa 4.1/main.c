#include <stdio.h>

int cubo(void); // Prototipo de función.

int I; // Variable global.

int main(void) {
    int CUB;
    for (I = 1; I <= 10; I++) {
        CUB = cubo(); // Llamada a la función cubo.
        printf("\nEl cubo de %d es: %d", I, CUB);
    }
    return 0;
}

int cubo(void) { // Declaración de la función.
    return (I * I * I);
}
